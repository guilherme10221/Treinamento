public class Operacoes {

	public static void main(String[] args) {
		
		int adicao = 5 + 2;
		int subtracao = 5 - 2;
		int multiplicacao = 5 * 2;
		int divisao = 10 / 2;
		int modulo = 10 % 3;
	
		// Exemplo de adição
		System.out.println("O resultado dessa operação é: 5 + 2 = "+adicao);
		
		// Exemplo de Subtração
		System.out.println("O resultado dessa operação é: 5 - 2 = "+subtracao);
		
	 	// Exemplo de Multiplicação
		System.out.println("O resultado dessa operação é: 5 * 2 = "+multiplicacao);
		
		// Exemplo de Divisão
		System.out.println("O resultado dessa operação é: 10 / 2 = "+divisao);
		
		// Exemplo de Módulo
//		 Equivale a 10 dividido por 3 que dá 
//		O módulo devolve o valor do resto
		System.out.println("O resultado dessa operação é: 10 % 3 = "+modulo);
		
		
		
		
		
		
	}
	
	
}
